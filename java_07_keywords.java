public class java_07_keywords {
    public static void main(String[] args) {
        // keywords in java

        // abstract continue for new switch
        // assert default goto package synchronized
        // boolean do if private this
        // break double implements protected throw
        // byte else import public throws
        // case enum instanceof return transient
        // catch extends int short try
        // char final interface static void
        // class finally long strictfp volatile
        // const float native super while

        // Note: goto and const are reserved keywords in Java but are not used.
        // They are reserved for future use.

        // Example usage of a keyword
        int number = 10; // 'int' is a keyword for integer data type
        if (number > 5) { // 'if' is a keyword for conditional statements
            System.out.println("Number is greater than 5");
        } else {
            System.out.println("Number is 5 or less");
        }

    }
}
