public class java_15_00_for_loop {
    
    public static void main(String[] args) {
    
        // loop through the array and print each element
        int[] numbers = {1, 2, 3, 4, 5};    
        for (int i = 0; i<=numbers.length; i++) {
            System.out.println(numbers[i]);

        }
    
    
    }
}
