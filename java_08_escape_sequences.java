public class java_08_escape_sequences {
    public static void main(String[] args) {
        // Escape Sequences in Java

        // New Line
        System.out.println("Hello,\nWorld!"); // \n for new line

        // Tab
        System.out.println("Hello,\tWorld!"); // \t for tab

        // Backslash
        System.out.println("This is a backslash: \\"); // \\ for backslash

        // Single Quote
        System.out.println("Single quote: \'"); // \' for single quote

        // Double Quote
        System.out.println("Double quote: \""); // \" for double quote

        // Carriage Return
        System.out.println("Hello, World!\rJava"); // \r for carriage return

        // Form Feed
        System.out.println("Hello,\fWorld!"); // \f for form feed

        // Backspace
        System.out.println("Hello,\bWorld!"); // \b for backspace
    }
}
